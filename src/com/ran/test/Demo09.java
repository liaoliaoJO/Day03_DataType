package com.ran.test;

public class Demo09 {
    public static void main(String[] args) {
        //关系运算符(比较运算符) --返回结果只能是布尔类型的值
        // ==:比较内存地址
        System.out.println(3 == 5);//false
        // !=:不相等   !(非)
        System.out.println(3 != 5);//true
        // >:大于
        System.out.println(3 > 5);//false
        // <:小于
        System.out.println(3 < 5);// true
        // >=:大于等于
        System.out.println(3 >= 5);//false
        // <=:小于等于
        System.out.println(3 <= 5);//true
    }
}
